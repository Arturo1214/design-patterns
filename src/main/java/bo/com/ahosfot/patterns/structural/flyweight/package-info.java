package bo.com.ahosfot.patterns.structural.flyweight;
/**
 Flyweight es un patrón de diseño estructural que permite a los programas soportar grandes cantidades
 de objetos manteniendo un bajo uso de memoria.

 El patrón lo logra compartiendo partes del estado del objeto entre varios objetos. En otras palabras,
 el Flyweight ahorra memoria RAM guardando en caché la misma información utilizada por distintos objetos.
 **/