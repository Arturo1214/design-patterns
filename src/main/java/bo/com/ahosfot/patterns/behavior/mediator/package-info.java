package bo.com.ahosfot.patterns.behavior.mediator;
/***
 Mediator es un patrón de diseño de comportamiento que reduce el acoplamiento entre
 los componentes de un programa haciendo que se comuniquen indirectamente a través de un objeto mediador especial.

 El patrón Mediator facilita la modificación, extensión y reutilización de componentes
 individuales porque ya no son dependientes de todas las demás clases.
 * **/